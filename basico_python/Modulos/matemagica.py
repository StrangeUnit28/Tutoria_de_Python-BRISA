"""Módulo criado para funções básicas da matemática"""
def soma(x, y):
    """Soma dois números"""
    return x + y

def sub(x, y):
    """Subtrai dois números"""
    return x - y

def div(x, y):
    """Divide dois números"""
    return x / y

def mul(x, y):
    """Multiplica dois números"""
    return x * y